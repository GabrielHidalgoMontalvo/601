    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed

        String nombre = this.txtNombre.getText();
        String mensajeEnviar = this.txtMensajeEnviar.getText();

        if (!nombre.isEmpty() && !mensajeEnviar.isEmpty()) {
            // Obtenemos la fecha y la formateamos como string
            Date fechaActual = new Date();
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            String fechaFormateada = formatoFecha.format(fechaActual);

            String mensaje = fechaFormateada + " - " + nombre + ": " + mensajeEnviar + "\n"; // Incorporamos la fecha al mensaje
            this.txtMensaje.append(mensaje);

            conversacion.registrarConversacion1(mensaje);

            Cliente cliente = new Cliente(6000, mensaje);
            Thread hilo = new Thread(cliente);
            hilo.start();
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

